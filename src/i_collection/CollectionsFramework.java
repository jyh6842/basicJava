package i_collection;

public class CollectionsFramework {
	
	/*
	 * << CollectionsFramework >>
	 * - Collections	: 다수의 데이터
	 * - Framework		: 표준화된 프로그래밍 방식
	 * << List >>
	 * - Vector		: ArrayList의 구버전 (ArrayList가 나오면서 사용 안함)
	 * - ArrayList	: 저장순서 유지, 중복 허용(읽는 시간이 빠르다)(LinkedList보다 많이 사용)
	 * - LinkedList	: 데이터 추가 및 삭제 시간 단축(추가와 삭제에서 빠르다)
	 * - Stack		: LIFO(Last In First Out)
	 * - Queue		: FIFO(First In First Out)
	 * 
	 * << Set >>
	 * - HashSet	: 중복 허용 안함, 저장순서 유지 안함
	 * - TreeSet	: 정렬, 검색 기능 향상
	 * 
	 * << Map >>
	 * - HashTabe	: HashMap의 구버전(HashMap을 사용한다)
	 * - HashMap	: 키(key)와 값(value)을 한쌍으로 저장(배열에서의 인덱스 번호가 아니라 키에 데이터를 저장한다)
	 * - TreeMap	: 정렬, 검색 기능 향상
	*/
}
